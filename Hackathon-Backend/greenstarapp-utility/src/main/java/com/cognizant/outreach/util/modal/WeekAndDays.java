package com.cognizant.outreach.util.modal;

import java.util.Date;

public class WeekAndDays {

	public String weekName;
	public Date startDate;
	public Date endDate;
	public int weekNumber;
	public String startDayName;
	public String endDayName;
	public String startDayShortName;
	public String endDayShortName;

	public int getWeekNumber() {
		return weekNumber;
	}

	public void setWeekNumber(int weekNumber) {
		this.weekNumber = weekNumber;
	}

	public String getStartDayName() {
		return startDayName;
	}

	public void setStartDayName(String startDayName) {
		this.startDayName = startDayName;
	}

	public String getEndDayName() {
		return endDayName;
	}

	public void setEndDayName(String endDayName) {
		this.endDayName = endDayName;
	}

	public String getStartDayShortName() {
		return startDayShortName;
	}

	public void setStartDayShortName(String startDayShortName) {
		this.startDayShortName = startDayShortName;
	}


	public String getEndDayShortName() {
		return endDayShortName;
	}

	public void setEndDayShortName(String endDayShortName) {
		this.endDayShortName = endDayShortName;
	}

	public String getWeekName() {
		return weekName;
	}

	public void setWeekName(String weekName) {
		this.weekName = weekName;
	}

	public Date getStartDate() {
		return startDate;
	}

	public void setStartDate(Date startDate) {
		this.startDate = startDate;
	}

	public Date getEndDate() {
		return endDate;
	}

	public void setEndDate(Date endDate) {
		this.endDate = endDate;
	}

}
